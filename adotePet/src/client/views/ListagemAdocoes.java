            int id = (int) defaultTablemModel.getValueAt( tblDadosAdocao.getSelectedRow(), 0);
            Adocao adocao = controller.getAdocaoById(id);
            
            TextView textView = new TextView( adocao.getInfo());
            textView.setVisible( true );
