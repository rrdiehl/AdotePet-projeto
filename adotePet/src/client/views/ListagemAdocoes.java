    private void refresh() 
    {
        defaultTablemModel.setRowCount(0);
        
        List<Adocao> adocoes = controller.getAdocoes();

        adocoes.forEach( adocao -> 
        {
            Object[] adocaoObject = {   adocao.getId(),
                                        adocao.getPetId(), 
                                        adocao.getTutorId(), 
                                        adocao.getDataSolicitacao().toString(), 
                                        adocao.getDataAdocao()!= null ? adocao.getDataAdocao().toString() :"",
                                        adocao.getStatusAdocao().toString()
                                     };
            defaultTablemModel.addRow(adocaoObject);
        } );
    }
