    public LocalDate formatData(String data) 
    {
        try 
        {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate date = LocalDate.parse(data, formatter);
            return date;
        } 
	catch ( DateTimeParseException e )
        {
            System.out.println("Erro na formatação da data: " + e.getMessage());
        }
        return null;
    }
    
    public String formatDataToPattern( LocalDate data )
    {
        try 
        {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern( "dd/MM/yyyy" );
            return formatter.format( data );
        } 
        catch (Exception e) 
        {
            System.out.println("Erro na formatação da data em formatDataToPattern");
        }
        return null;
    }

