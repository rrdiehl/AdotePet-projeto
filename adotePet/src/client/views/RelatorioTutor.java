    private void btnGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarActionPerformed
        
        LinkedList registers = new LinkedList();
        
        if( !jFormattedTextFieldDe.getText().equals("  /  /    ") && !jFormattedTextFieldAte.getText().equals("  /  /    ") )
        {
            LocalDate dataStart = formatData( jFormattedTextFieldDe.getText() );
            LocalDate dataEnd = formatData( jFormattedTextFieldAte.getText() );
            
            for( Tutor tutor : tutorController.buscarTodosTutores() )
            {
                if( tutor.getDataDeRegistro().toLocalDate().isEqual(dataStart) || tutor.getDataDeRegistro().toLocalDate().isAfter(dataStart ) )
                {
                    if( tutor.getDataDeRegistro().toLocalDate().isBefore( dataEnd ) || tutor.getDataDeRegistro().toLocalDate().isEqual( dataEnd ) )
                    {
                        registers.add( tutor.getId() );
                        registers.add( tutor.getNome() );
                        registers.add( tutor.getEmail() );
                        registers.add( formatDataToPattern(tutor.getDataDeRegistro().toLocalDate()) );
                        
                    }
                }
            }
        }
        else
        {
            for( Tutor tutor : tutorController.buscarTodosTutores() )
            {
                registers.add( tutor.getId() );
                registers.add( tutor.getNome() );
                registers.add( tutor.getEmail() );
                registers.add( formatDataToPattern(tutor.getDataDeRegistro().toLocalDate()) );
            }
        }
        
                GerarPdf.gerarRelatorioPdf("Relatório de Tutores", columns, registers, new float[]{10,40,30,20} );

    }//GEN-LAST:event_btnGerarActionPerformed

