    public enum Acao
    {
        CADASTRAR,
        EDITAR
    }
    
    public CadastroPet( Acao acao, Pet pet )
    {
        this();
         
        this.acao = acao;
        this.pet = pet;
        
        if ( pet != null && acao == Acao.EDITAR )
        {
            txtNome.setText( pet.getNome() );
            txtImagemSrc.setText( pet.getImagem() );
            cbxTipo.setSelectedItem( pet.getTipoPet() );
            cbxPorte.setSelectedItem( pet.getTipoPorte() );
            idadeSpinner.setValue( pet.getIdade() );
        }  
    }
    
