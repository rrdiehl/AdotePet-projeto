    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if ( !txtNome.getText().isEmpty() && !txtImagemSrc.getText().isEmpty() )
        {
            pet.setNome( txtNome.getText());
            pet.setIdade( (int) idadeSpinner.getValue() );
            pet.setImagem( txtImagemSrc.getText() );
            pet.setTipoPet( cbxTipo.getSelectedIndex() == 0?  TipoPet.GATO : TipoPet.CACHORRO );
            pet.setTipoPorte( cbxPorte.getSelectedIndex() == 0? TipoPorte.PEQUENO :
                              cbxPorte.getSelectedIndex() == 1? TipoPorte.MEDIO : TipoPorte.GRANDE);
            
            JOptionPane.showMessageDialog( this, acao == Acao.EDITAR? controller.atualizarPet(pet) :controller.cadastrar( pet ) ); 
            
            if ( acao == Acao.EDITAR )
            {
                ListagemPet listagem = new ListagemPet();
                
                listagem.setVisible( true );
                dispose();
            }   
        }
        
        else
        {
            JOptionPane.showMessageDialog( this, "Todos os campos devem estar preenchidos");   
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

