    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!txtEmail.getText().isEmpty() && !txtSenha.getText().isEmpty()) {
            user.setEmail(txtEmail.getText());
            user.setSenha(txtSenha.getText());

            JOptionPane.showMessageDialog(this, acao == Acao.EDITAR ? controller.atualizarLogin(user) : controller.cadastrar(user));

            if (acao == Acao.EDITAR) {
                ListagemUsuario listagem = new ListagemUsuario();

                listagem.setVisible(true);
                dispose();
            } else {
                refresh();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Todos os campos devem estar preenchidos");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

