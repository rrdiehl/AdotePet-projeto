    public ImagemPet( String imagemSrc) {
        initComponents();
        
        if ( imagemSrc != null )
        {
            try 
            {
                File file = new File(imagemSrc);
                
                if ( file.exists() )
                {
                    BufferedImage img = ImageIO.read( file );
            
                    ImageIcon icon = new ImageIcon(img);
                    imagem.setIcon(icon);

                    getContentPane().add(imagem, BorderLayout.CENTER);

                    setVisible(true);
                }
            } 
            
            catch (IOException ex) 
            {
                Logger.getLogger(ImagemPet.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
       

