    private LocalDate convertStringToLocalDateTime(String dateString) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        try {
            return LocalDate.parse(dateString, formatter);
        } catch (DateTimeParseException e) {
            System.err.println("Formato de data inválido: " + dateString);
            return null;
        }
    }
     
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        LocalDate dataSolicitacao = convertStringToLocalDateTime( txtDataSolicitacao.getText() );
        if ( !txtInfo.getText().isEmpty() && !txtJustificativa.getText().isEmpty() && dataSolicitacao!= null )
        {
            if ( adocao == null )
            {
                adocao = new Adocao();
            }
            
            adocao.setPetId( Integer.parseInt((String) cbxPet.getSelectedItem()));
            adocao.setTutorId(Integer.parseInt((String) cbxTutor.getSelectedItem() ));
            adocao.setDataSolicitacao( dataSolicitacao );
            adocao.setStatusAdocao( Adocao.StatusAdocao.values()[ cbxStatus.getSelectedIndex()]);
            adocao.setJustificativa( txtJustificativa.getText());
            adocao.setInfo(txtInfo.getText());
            
            if( !txtDataAdocao.getText().isBlank() )
            {
                adocao.setDataAdocao( convertStringToLocalDateTime( txtDataAdocao.getText() ) );
            }
            
            JOptionPane.showMessageDialog( this, acao == Acao.EDITAR? controller.atualizar(adocao) :controller.cadastrar( adocao ) ); 
            
            if ( acao == Acao.EDITAR )
            {
                ListagemAdocoes listagem = new ListagemAdocoes();
                
                listagem.setVisible( true );
                dispose();
            }   
            
            else   
            {
                refresh();
            }
        }
        
        else
        {
            JOptionPane.showMessageDialog( this, "Todos os campos devem estar preenchidos corretamente");   
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

