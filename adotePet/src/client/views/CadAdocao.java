    public CadAdocao( Acao acao, Adocao adocao ) {
        initComponents();
        
        this.acao = acao;
        this.adocao = adocao;
        
        if ( adocao != null && acao != null && acao == Acao.EDITAR )
        {
            cbxPet.addItem( String.valueOf(adocao.getPetId() ));
            cbxTutor.addItem( String.valueOf(adocao.getTutorId() ));
        }
        
        for( Pet pet : petController.buscarTodosPetsDisponiveis() )
        {
            cbxPet.addItem( String.valueOf( pet.getId() ) );
        }
        
        for( Tutor tutor : tutorController.buscarTodosTutoresDisponiveis() )
        {
            cbxTutor.addItem( String.valueOf( tutor.getId() ) );
        }
        
        for( Adocao.StatusAdocao status : Adocao.StatusAdocao.values() )
        {
            cbxStatus.addItem( status.toString() );
        }
        
        if ( adocao != null && acao == Acao.EDITAR )
        { 
            cbxStatus.setSelectedIndex( adocao.getStatusAdocao().ordinal());
            txtInfo.setText( adocao.getInfo());
            txtJustificativa.setText( adocao.getJustificativa());
            txtDataAdocao.setText( adocao.getDataAdocao() != null ? adocao.getDataAdocao().format(DateTimeFormatter.ofPattern("yyyy-MM-dd")): "");
            txtDataSolicitacao.setText( adocao.getDataSolicitacao().format(DateTimeFormatter.ofPattern("yyyy-MM-dd") ));
        }
    }
    
    public void refresh()
    {
        cbxPet.setSelectedIndex(0);
        cbxTutor.setSelectedIndex(0 );
        cbxStatus.setSelectedIndex( adocao.getStatusAdocao().ordinal());
        txtInfo.setText( "");
        txtJustificativa.setText( "");
        txtDataSolicitacao.setText( "");
        txtDataAdocao.setText( "");
    }

