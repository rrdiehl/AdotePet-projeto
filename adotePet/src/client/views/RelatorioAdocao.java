    public LocalDate formatData(String data) 
    {
        try 
        {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate date = LocalDate.parse(data, formatter);
            return date;
        } 
	catch ( DateTimeParseException e )
        {
            System.out.println("Erro na formatação da data: " + e.getMessage());
        }
        return null;
    }
    
    public boolean verificaData( LocalDate data1, LocalDate data2, LocalDate data )
    {
        if( data1.isEqual( data ) || data2.isEqual( data )  ) return true;
        if( data1.isBefore(data ) && data2.isAfter( data ) ) return true;
        
        return false;
    }
    
    public void addRegisters( LinkedList registers, Adocao adocao, Pet pet, Tutor tutor )
    {
        registers.add( adocao.getId());
        registers.add( pet.getNome() + "[" + pet.getId() + "]" );
        registers.add( tutor.getNome() + "[" + pet.getId() + "]" );
        registers.add( adocao.getDataSolicitacao() != null ? adocao.getDataSolicitacao().toString() : "null" );
        registers.add( adocao.getDataAdocao() != null ? adocao.getDataAdocao().toString() : "null" );
        registers.add( adocao.getStatusAdocao().toString() );
        registers.add( adocao.getInfo() );
        registers.add( adocao.getJustificativa() );
    }

