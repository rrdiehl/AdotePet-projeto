    private void btnGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarActionPerformed
        
        boolean isAll = jComboBoxStatus.getSelectedIndex() > 0 ? false : true;
        boolean isDtSolicitacao  = jFormattedTextDe.getText().equals("  /  /    ") ? false : true;
        boolean isDtSolicitacaoAte  = jFormattedTextAte.getText().equals("  /  /    ") ? false : true;
        boolean isDtAdocao  = jFormattedTextDe1.getText().equals("  /  /    ") ? false : true;
        boolean isDtAdocaoAte  = jFormattedTextAte1.getText().equals("  /  /    ") ? false : true;
        
        LinkedList registers = new LinkedList();
        
        LocalDate dtSolicitacao = null;
        LocalDate dtSolicitacaoAte = null;
        LocalDate dtAdocao = null;
        LocalDate dtAdocaoAte = null;
        
        if( isDtSolicitacao && isDtSolicitacaoAte )
        {
            dtSolicitacao = formatData( jFormattedTextDe.getText() );
            dtSolicitacaoAte = formatData( jFormattedTextAte.getText() );
        }
        
        if( isDtAdocao && isDtAdocaoAte )
        {
            dtAdocao = formatData( jFormattedTextDe1.getText() );
            dtAdocaoAte = formatData( jFormattedTextAte1.getText() );
        }
        
        if( !isAll )
        {
            for( Adocao adocao : adocaoController.getAdocoes() )
            {
                if( adocao.getStatusAdocao().equals( Adocao.StatusAdocao.values()[ jComboBoxStatus.getSelectedIndex() -1 ] ) )
                {
                    Tutor tutor = tutorController.buscartTtorPorId( adocao.getTutorId() );
                    Pet pet = petController.buscarPetPorId( adocao.getPetId() );
                    
                    if( isDtSolicitacao && adocao.getDataSolicitacao() != null && verificaData( dtSolicitacao, dtSolicitacaoAte, adocao.getDataSolicitacao() ) )
                    {
                        if( isDtAdocao && adocao.getDataAdocao() != null && verificaData( dtAdocao, dtAdocaoAte, adocao.getDataAdocao() ) )
                        {
                            addRegisters( registers, adocao, pet, tutor );
                        }
                        else if( !isDtAdocao )
                        {
                            addRegisters( registers, adocao, pet, tutor );
                        }
                    }
                    else if( isDtAdocao && adocao.getDataAdocao() != null && verificaData( dtAdocao, dtAdocaoAte, adocao.getDataAdocao() ) )
                    {
                        addRegisters( registers, adocao, pet, tutor );

                    }
                    else if( !isDtSolicitacao && !isDtAdocao )
                    {
                        addRegisters( registers, adocao, pet, tutor );
                    }
                }
            }
        }
        else
        {
            for( Adocao adocao : adocaoController.getAdocoes() )
            {
                Tutor tutor = tutorController.buscartTtorPorId( adocao.getTutorId() );
                Pet pet = petController.buscarPetPorId( adocao.getPetId() );

                if( isDtSolicitacao && adocao.getDataSolicitacao() != null && verificaData( dtSolicitacao, dtSolicitacaoAte, adocao.getDataSolicitacao() ) )
                {
                    if( isDtAdocao && adocao.getDataAdocao() != null && verificaData( dtAdocao, dtAdocaoAte, adocao.getDataAdocao() ) )
                    {
                        addRegisters( registers, adocao, pet, tutor );
                    }
                    else if( !isDtAdocao )
                    {
                        addRegisters( registers, adocao, pet, tutor );
                    }
                }
                else if( isDtAdocao && adocao.getDataAdocao() != null && verificaData( dtAdocao, dtAdocaoAte, adocao.getDataAdocao() ) )
                {
                    addRegisters( registers, adocao, pet, tutor );

                }
                else if( !isDtSolicitacao && !isDtAdocao )
                {
                    addRegisters( registers, adocao, pet, tutor );
                }
            }
        }
        GerarPdf.gerarRelatorioPdf("Relatório de Adoção", columns, registers, new float[]{5,10,15,15,15,15,15,10} );
    }//GEN-LAST:event_btnGerarActionPerformed

